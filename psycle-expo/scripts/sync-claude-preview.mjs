#!/usr/bin/env node

import { execFileSync } from "node:child_process";
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const appRoot = path.resolve(scriptDir, "..");
const workspaceRoot = path.resolve(process.cwd());
const monorepoRoot = execFileSync(
  "git",
  ["-C", workspaceRoot, "rev-parse", "--show-toplevel"],
  {
    encoding: "utf8",
    stdio: ["ignore", "pipe", "pipe"],
  },
).trim();
const remoteRef = "origin/claude/evaluate-probabilistic-fix-UXBGS";

const remoteCandidates = [
  "psycle-expo/design-previews/trail-vision-v2.html",
  "psycle-expo/design-previews/trail-vision.html",
  "psycle-expo/design-previews/claude/mockup-preview.html",
];

const outputHtmlPath = path.join(
  appRoot,
  "design-previews",
  "claude",
  "mockup-preview.html",
);
const generatedTsPath = path.join(
  appRoot,
  "app",
  "debug",
  "generated",
  "claude-preview.ts",
);

function runGit(args, options = {}) {
  return execFileSync("git", ["-C", monorepoRoot, ...args], {
    encoding: "utf8",
    stdio: ["ignore", "pipe", "pipe"],
    ...options,
  });
}

function readRemoteFile(ref, filePath) {
  try {
    return runGit(["show", `${ref}:${filePath}`]);
  } catch {
    return null;
  }
}

function readRemoteFileCommitEpoch(ref, filePath) {
  try {
    const output = runGit([
      "log",
      "-1",
      "--format=%ct",
      ref,
      "--",
      filePath,
    ]).trim();
    return output ? Number(output) : null;
  } catch {
    return null;
  }
}

function toTsTemplateString(source) {
  return source.replace(/\\/g, "\\\\").replace(/`/g, "\\`").replace(/\$\{/g, "\\${");
}

try {
  runGit(["fetch", "origin", "claude/evaluate-probabilistic-fix-UXBGS"], {
    stdio: "inherit",
  });

  const commit = runGit(["rev-parse", remoteRef]).trim().slice(0, 7);

  const previewCandidates = remoteCandidates
    .map((candidate) => ({
      candidate,
      updatedAt: readRemoteFileCommitEpoch(remoteRef, candidate),
    }))
    .filter((entry) => entry.updatedAt !== null)
    .sort((a, b) => (b.updatedAt ?? 0) - (a.updatedAt ?? 0));

  let sourcePath = "";
  let html = "";
  for (const entry of previewCandidates) {
    const maybe = readRemoteFile(remoteRef, entry.candidate);
    if (maybe) {
      sourcePath = entry.candidate;
      html = maybe;
      break;
    }
  }

  if (!html) {
    throw new Error(
      `Could not find a Claude preview HTML in ${remoteRef}. Checked: ${remoteCandidates.join(", ")}`,
    );
  }

  fs.mkdirSync(path.dirname(outputHtmlPath), { recursive: true });
  fs.mkdirSync(path.dirname(generatedTsPath), { recursive: true });

  fs.writeFileSync(outputHtmlPath, html, "utf8");

  const generatedSource = [
    "// Generated by scripts/sync-claude-preview.mjs",
    `export const claudePreviewCommit = ${JSON.stringify(commit)};`,
    `export const claudePreviewSource = ${JSON.stringify(sourcePath)};`,
    `export const claudePreviewHtml = String.raw\`${toTsTemplateString(html)}\`;`,
    "",
  ].join("\n");

  fs.writeFileSync(generatedTsPath, generatedSource, "utf8");

  process.stdout.write(
    `Synced Claude preview ${commit} from ${sourcePath}\n` +
      `- HTML: ${outputHtmlPath}\n` +
      `- Generated: ${generatedTsPath}\n`,
  );
} catch (error) {
  process.stderr.write(
    `${error instanceof Error ? error.message : String(error)}\n`,
  );
  process.exit(1);
}
